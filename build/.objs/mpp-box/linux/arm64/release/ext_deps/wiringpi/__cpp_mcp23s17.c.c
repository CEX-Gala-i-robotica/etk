static void writeByte (uint8_t spiPort, uint8_t devId, uint8_t reg, uint8_t data)
{
  uint8_t spiData [4] ;

  spiData [0] = 0x40 | ((devId & 7) << 1) ;
  spiData [1] = reg ;
  spiData [2] = data ;

  wiringPiSPIDataRW (spiPort, spiData, 3) ;
}







static uint8_t readByte (uint8_t spiPort, uint8_t devId, uint8_t reg)
{
  uint8_t spiData [4] ;

  spiData [0] = 0x41 | ((devId & 7) << 1) ;
  spiData [1] = reg ;

  wiringPiSPIDataRW (spiPort, spiData, 3) ;

  return spiData [2] ;
}







static void myPinMode (struct wiringPiNodeStruct *node, int pin, int mode)
{
  int mask, old, reg ;

  pin -= node->pinBase ;

  if (pin < 8)
    reg = 0x00 ;
  else
  {
    reg = 0x01 ;
    pin &= 0x07 ;
  }

  mask = 1 << pin ;
  old = readByte (node->data0, node->data1, reg) ;

  if (mode == 1)
    old &= (~mask) ;
  else
    old |= mask ;

  writeByte (node->data0, node->data1, reg, old) ;
}







static void myPullUpDnControl (struct wiringPiNodeStruct *node, int pin, int mode)
{
  int mask, old, reg ;

  pin -= node->pinBase ;

  if (pin < 8)
    reg = 0x0C ;
  else
  {
    reg = 0x0D ;
    pin &= 0x07 ;
  }

  mask = 1 << pin ;
  old = readByte (node->data0, node->data1, reg) ;

  if (mode == 2)
    old |= mask ;
  else
    old &= (~mask) ;

  writeByte (node->data0, node->data1, reg, old) ;
}







static void myDigitalWrite (struct wiringPiNodeStruct *node, int pin, int value)
{
  int bit, old ;

  pin -= node->pinBase ;

  bit = 1 << (pin & 7) ;

  if (pin < 8)
  {
    old = node->data2 ;

    if (value == 0)
      old &= (~bit) ;
    else
      old |= bit ;

    writeByte (node->data0, node->data1, 0x12, old) ;
    node->data2 = old ;
  }
  else
  {
    old = node->data3 ;

    if (value == 0)
      old &= (~bit) ;
    else
      old |= bit ;

    writeByte (node->data0, node->data1, 0x13, old) ;
    node->data3 = old ;
  }
}







static int myDigitalRead (struct wiringPiNodeStruct *node, int pin)
{
  int mask, value, gpio ;

  pin -= node->pinBase ;

  if (pin < 8)
    gpio = 0x12 ;
  else
  {
    gpio = 0x13 ;
    pin &= 0x07 ;
  }

  mask = 1 << pin ;
  value = readByte (node->data0, node->data1, gpio) ;

  if ((value & mask) == 0)
    return 0 ;
  else
    return 1 ;
}
