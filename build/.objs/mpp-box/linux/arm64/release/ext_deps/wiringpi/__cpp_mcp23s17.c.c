int mcp23s17Setup (const int pinBase, const int spiPort, const int devId)
{
  struct wiringPiNodeStruct *node ;

  if (wiringPiSPISetup (spiPort, 4000000) < 0)
    return (!(1==1)) ;

  writeByte (spiPort, devId, 0x0A, (0x20) | 0x08) ;
  writeByte (spiPort, devId, 0x0B, (0x20) | 0x08) ;

  node = wiringPiNewNode (pinBase, 16) ;

  node->data0 = spiPort ;
  node->data1 = devId ;
  node->pinMode = myPinMode ;
  node->pullUpDnControl = myPullUpDnControl ;
  node->digitalRead = myDigitalRead ;
  node->digitalWrite = myDigitalWrite ;
  node->data2 = readByte (spiPort, devId, 0x14) ;
  node->data3 = readByte (spiPort, devId, 0x15) ;

  return (1==1) ;
}
